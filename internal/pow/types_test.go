package pow_test

import (
	"testing"

	"github.com/stretchr/testify/assert"

	"faraway/internal/pow"
)

func TestSolution_Difficulty(t *testing.T) {
	t.Run("difficulty 0", func(t *testing.T) {
		solution := pow.Solution{
			0x80, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		}

		difficulty := solution.Difficulty()

		assert.Equal(t, 0, int(difficulty))
	})

	t.Run("difficulty 7", func(t *testing.T) {
		solution := pow.Solution{
			0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		}

		difficulty := solution.Difficulty()

		assert.Equal(t, 7, int(difficulty))
	})

	t.Run("difficulty 255", func(t *testing.T) {
		solution := pow.Solution{
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,
		}

		difficulty := solution.Difficulty()

		assert.Equal(t, 255, int(difficulty))
	})

	t.Run("difficulty 256", func(t *testing.T) {
		solution := pow.Solution{
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		}

		difficulty := solution.Difficulty()

		assert.Equal(t, 256, int(difficulty))
	})
}
